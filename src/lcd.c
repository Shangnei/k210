void lcd_draw_char(uint16_t x, uint16_t y, char c, uint16_t color) {
  uint8_t i = 0;
  uint8_t j = 0;
  uint8_t data = 0;
  // c代表字符一个字符8*16，以ascall码表存储
  for (i = 0; i < 16; i++) {
    data = ascii0816[c * 16 + i];
    for (j = 0; j < 8; j++) {
      if (data & 0x80) draw_point(x + j, y, color);  //每次选择最高位做判断
      data <<= 1;
    }
    y++;
  }
}
void lcd_draw_string(uint16_t x, uint16_t y, char *str, uint16_t color) {
  while (*str) {  //用数组来判断边界，字符加一就是下一个字符
    lcd_draw_char(x, y, *str, color);
    str++;
    x += 8;
  }
}
