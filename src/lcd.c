void io_init(void) {
  fpioa_set_function(39, FUNC_SPI0_SCLK);  // wr
  fpioa_set_function(38, FUNC_GPIOHS0);    // dc
  fpioa_set_function(37, FUNC_GPIOHS1);    // rst
  fpioa_set_function(36, sc);              //片选ss1；
  sysctl_set_spi0_dvp_data(1);  //开启spio 的传输通道，必须有，但没查到具体解释
}  //初始化硬件io
void st7789_init(void) {
  gpiohs_set_drive_mode(0, GPIO_DM_OUTPUT);
  gpiohs_set_pin(0, 1);  //设置gpio为输出模式，
  gpiohs_set_drive_mode(1, GPIO_DM_OUTPUT);
  gpiohs_set_pin(1, GPIO_PV_HIGH);  // too
  // gpiohs_set_pin(1, 0);             //可有可无
  spi_init(SPI_DEVICE_0, SPI_WORK_MODE_1, SPI_FF_OCTAL, 8, 0);
  spi_set_clk_rate(SPI_DEVICE_0, 20000000);
  gpiohs_set_pin(1, 1);
}
void set_cmd(uint8_t cmd) {
  gpiohs_set_pin(0, 0);
  spi_init(SPI_DEVICE_0, SPI_WORK_MODE_0, SPI_FF_OCTAL, 8,
           0);  //初始化8线，必须有应为可能工作在不同的spl mode
  spi_init_non_standard(
      SPI_DEVICE_0, 8, 0, 0,
      SPI_AITM_AS_FRAME_FORMAT);  //初始化8位发送，要用非标准的可能是因为没有地址
  spi_send_data_normal_dma(DMAC_CHANNEL0, SPI_DEVICE_0, cs, (uint8_t *)(&cmd),
                           1,
                           0x1);  //用0x2也可以但感觉还是不对
}  // write cmd
void set_data(uint8_t *data, uint8_t len) {
  gpiohs_set_pin(0, 1);
  spi_init(SPI_DEVICE_0, SPI_WORK_MODE_0, SPI_FF_OCTAL, 8, 0);
  spi_init_non_standard(SPI_DEVICE_0, 8, 0, 0,
                        SPI_AITM_AS_FRAME_FORMAT);  //初始化8位发送，待测试
  spi_send_data_normal_dma(DMAC_CHANNEL0, SPI_DEVICE_0, cs, data, len,
                           SPI_TRANS_CHAR);
}  // writh 1byte
void lcd_set_direction(lcd_dir_t dir) {
  lcd_ctl.dir = dir;
  if (dir & DIR_XY_MASK) {
    lcd_ctl.width = LCD_Y_MAX - 1;
    lcd_ctl.height = LCD_X_MAX - 1;
  } else {
    lcd_ctl.width = LCD_X_MAX - 1;
    lcd_ctl.height = LCD_Y_MAX - 1;  //应该是保存实时坐标
  }

  set_cmd(MEMORY_ACCESS_CTL);    //设置内存命令
  set_data((uint8_t *)&dir, 1);  //设置扫描方向
}
void lcd_set_area(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2) {
  uint8_t data[4] = {0};

  data[0] = (uint8_t)(x1 >> 8);
  data[1] = (uint8_t)(x1);
  data[2] = (uint8_t)(x2 >> 8);
  data[3] = (uint8_t)(x2);
  set_cmd(HORIZONTAL_ADDRESS_SET);  //行设置命令，数据手册，16位起始，终点
  set_data(data, 4);

  data[0] = (uint8_t)(y1 >> 8);
  data[1] = (uint8_t)(y1);
  data[2] = (uint8_t)(y2 >> 8);  // too
  data[3] = (uint8_t)(y2);
  set_cmd(VERTICAL_ADDRESS_SET);
  set_data(data, 4);

  set_cmd(MEMORY_WRITE);  //开启传输数据，为帧显示
}
void tft_fill_data(uint32_t *data_buf, uint32_t length) {
  gpiohs_set_pin(0, 1);
  spi_init(SPI_CHANNEL, SPI_WORK_MODE_0, SPI_FF_OCTAL, 32, 0);
  spi_init_non_standard(0, 0, 32, 0, SPI_AITM_AS_FRAME_FORMAT);
  spi_fill_data_dma(
      DMAC_CHANNEL0, SPI_DEVICE_0, cs, data_buf,
      length);  //应该是自动填充所以用32位的,破案了，因为dma接收32位说
}
void lcd_clear(uint16_t color) {
  uint32_t data = ((uint32_t)color << 16) | (uint32_t)color;
  lcd_set_area(0, 0, lcd_ctl.height,
               lcd_ctl.width);  //颜色是16位所以这样道理上来讲no question
  tft_fill_data(&data, LCD_X_MAX * LCD_Y_MAX / 2);
}
void lcd_init(void) {
  uint8_t data = 0;
  /* 硬件初始化 */
  io_init();
  /* 重置LCD */
  st7789_init();
  set_cmd(SOFTWARE_RESET);
  usleep(100000);
  /* 关闭睡眠模式 */
  set_cmd(SLEEP_OFF);
  usleep(100000);
  /* 设置像素格式：65K, 16bit/pixel */
  set_cmd(PIXEL_FORMAT_SET);
  data = 0x05;  //经查跟565显示有关，im 应该是原理图的连接模式，待查阅
  set_data(&data, 1);
  /* 设置LCD显示方向 */
  lcd_set_direction(DIR_YX_LRUD);

  /* 使能显示 */
  set_cmd(DISPLAY_ON);
  /* 清空显示 */
  lcd_clear(WHITE);
}
void draw_point(uint16_t x, uint16_t y, uint16_t colocr) {
  lcd_set_area(x, y, x, y);
  write_16bit(&colocr, 1);
}
void write_16bit(uint16_t *data_buf, uint32_t length) {
  gpiohs_set_pin(0, 1);
  spi_init(SPI_CHANNEL, SPI_WORK_MODE_0, SPI_FF_OCTAL, 16, 0);
  spi_init_non_standard(0, 16, 0, 0, SPI_AITM_AS_FRAME_FORMAT);
  spi_send_data_normal_dma(0, 0, cs, data_buf, length, 2);
}
void write_32bit(uint32_t *data_buf, uint32_t length) {
  gpiohs_set_pin(0, 1);
  spi_init(SPI_CHANNEL, SPI_WORK_MODE_0, SPI_FF_OCTAL, 32, 0);
  spi_init_non_standard(0, 0, 32, 0, SPI_AITM_AS_FRAME_FORMAT);
  spi_send_data_normal_dma(0, 0, cs, data_buf, length, SPI_TRANS_INT);
}
void swap(uint16_t *x, uint16_t *y) {
  *x = *x ^ *y;
  *y = *x ^ *y;
  *x = *x ^ *y;
}
void draw_line(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2,
               uint16_t color) {
  uint16_t flag, temp, dx, dy, slope, mid;
  flag = (y2 - y1) > (x2 - x1);
  if (flag) {
    swap(&x1, &y1);
    swap(&x2, &y2);
  }
  dx = (x2 - x1);
  dy = (y2 - y1);
  slope = 2 * dy;
  mid = slope;
  while (x1 <= x2) {
    if (flag)
      draw_point(y1, x1, color);
    else
      draw_point(x1, y1, color);
    x1++;
    if (mid > dx) {
      y1++;
      mid -= 2 * dx;
    }
    mid += slope;
  }
}
void lcd_draw_picture(uint16_t x1, uint16_t y1, uint16_t width, uint16_t height,
                      uint32_t *ptr) {
  lcd_set_area(x1, y1, x1 + width - 1, y1 + height - 1);
  write_32bit(ptr, width * height / 2);
}
void lcd_draw_rectangle(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2,
                        uint16_t width, uint16_t color) {
  uint32_t data_buf[640] = {0};
  uint32_t *p = data_buf;
  uint32_t data = color;
  uint32_t index = 0;

  data = (data << 16) | data;
  for (index = 0; index < 160 * width; index++) *p++ = data;

  lcd_set_area(x1, y1, x2, y1 + width - 1);
  write_32bit(data_buf, ((x2 - x1 + 1) * width + 1) / 2);
  lcd_set_area(x1, y2 - width + 1, x2, y2);
  write_32bit(data_buf, ((x2 - x1 + 1) * width + 1) / 2);
  lcd_set_area(x1, y1, x1 + width - 1, y2);
  write_32bit(data_buf, ((y2 - y1 + 1) * width + 1) / 2);
  lcd_set_area(x2 - width + 1, y1, x2, y2);
  write_32bit(data_buf, ((y2 - y1 + 1) * width + 1) / 2);
}
